// These are locations where parsed parameters from the data
// stream are stored.
#define PARAMS 4
static int P[PARAMS];
static int P_size = 0;

static int p1(int nominal)
{
  if (P_size >= 1) {
    return P[0];
  }
  else
    return nominal;
}

static int p2(int nominal)
{
  if (P_size >= 2)
    return P[1];
  else
    return nominal;
}

